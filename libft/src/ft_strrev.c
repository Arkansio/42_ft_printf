#include "libft.h"

void	ft_strrev(char *str)
{
	int		size;
	char	tmp;
	int		i;

	i = 0;
	if (!str)
		return ;
	size = ft_strlen(str);
	while (i < size / 2)
	{
		tmp = str[i];
		str[i] = str[size - i - 1];
		str[size - i - 1] = tmp;
		i++;
	}
}
